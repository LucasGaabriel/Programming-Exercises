package org.example

import kotlin.math.*

fun main() {
    // Arredondando valores
    println("- Round de 6.14: ${round(6.14)}")
    println("- Ceil de 6.14: ${ceil(6.14)}")
    println("- Floor de 6.14: ${floor(6.14)}")
    println()

    println(PI)
    println(abs(-35.4))
    println(ln(53.2))
    println(truncate(58.956))
    println(sqrt(256.0))
    println(round(sin(PI)))
    println(cos(PI))
    println(max(2, 8))
    println(min(47, 65))
}
